//Variáveis
let faseAtual = 0;
let posicaoJogador = { linha: 1, coluna: 1 };
let elementoLabirinto = document.getElementById("labirinto");

//Login
document.getElementById("formLogin").addEventListener("submit", function (e) {
    e.preventDefault();
    const nome = document.getElementById("nomeJogador").value;
    document.getElementById("exibeNome").textContent = "Jogador: " + nome;
    mostrarTela("telaJogo");
    carregarLabirinto();
});

//Funçoes
function mostrarTela(idTela) {
    document.querySelectorAll(".tela").forEach(t => t.classList.remove("ativa"));
    document.getElementById(idTela).classList.add("ativa");
}

function carregarLabirinto() {
    elementoLabirinto.innerHTML = "";
    const lab = labirintos[faseAtual];
    document.getElementById("exibeFase").textContent = "Labirinto - Fase " + (faseAtual + 1) + " de 3";

    for (let l = 0; l < 20; l++) {
        for (let c = 0; c < 20; c++) {
            const celula = document.createElement("div");
            celula.classList.add("celula");
            if (lab[l][c] === 1) celula.classList.add("parede");
            else celula.classList.add("livre");
            if (lab[l][c] === 2) celula.classList.add("saida");
            if (l === posicaoJogador.linha && c === posicaoJogador.coluna) celula.classList.add("jogador");
            elementoLabirinto.appendChild(celula);
        }
    }
}

function moverJogador(dl, dc) {
    const lab = labirintos[faseAtual];
    const novaLinha = posicaoJogador.linha + dl;
    const novaColuna = posicaoJogador.coluna + dc;

    if (lab[novaLinha][novaColuna] !== 1) {
        posicaoJogador = { linha: novaLinha, coluna: novaColuna };

        if (lab[novaLinha][novaColuna] === 2) {
            faseAtual++;
            if (faseAtual >= labirintos.length) {
                mostrarTela("telaFinal");
                return;
            }
            posicaoJogador = { linha: 1, coluna: 1 };
        }
        carregarLabirinto();
    }
}

//Movimentação
document.addEventListener("keydown", (e) => {
    if (["ArrowUp", "ArrowDown", "ArrowLeft", "ArrowRight"].includes(e.key)) {
        e.preventDefault();
    }

    if (e.key === "ArrowUp") moverJogador(-1, 0);
    else if (e.key === "ArrowDown") moverJogador(1, 0);
    else if (e.key === "ArrowLeft") moverJogador(0, -1);
    else if (e.key === "ArrowRight") moverJogador(0, 1);
});