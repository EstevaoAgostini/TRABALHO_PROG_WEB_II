let faseAtual = 0;
let posicaoJogador = { linha: 1, coluna: 1 };
let nomeJogador = "";

function mostrarTela(id) {
    document.querySelectorAll(".tela").forEach(tela => tela.classList.remove("ativa"));
    document.getElementById(id).classList.add("ativa");
}

function carregarLabirinto() {
    const lab = labirintos[faseAtual];
    const elementoLabirinto = document.getElementById("labirinto");
    elementoLabirinto.innerHTML = "";
    document.getElementById("exibeFase").innerText = "Fase " + (faseAtual + 1) + " de 3";

    for (let l = 0; l < 20; l++) {
        for (let c = 0; c < 20; c++) {
            const celula = document.createElement("div");
            celula.classList.add("celula");
            if (lab[l][c] === 1) celula.classList.add("parede");
            else celula.classList.add("livre");
            if (lab[l][c] === 2) celula.classList.add("saida");
            if (l === posicaoJogador.linha && c === posicaoJogador.coluna) celula.classList.add("jogador");
            elementoLabirinto.appendChild(celula);
        }
    }
}

function moverJogador(dl, dc) {
    const lab = labirintos[faseAtual];
    const novaLinha = posicaoJogador.linha + dl;
    const novaColuna = posicaoJogador.coluna + dc;

    if (lab[novaLinha][novaColuna] !== 1) {
        posicaoJogador = { linha: novaLinha, coluna: novaColuna };

        if (lab[novaLinha][novaColuna] === 2) {
            faseAtual++;
            if (faseAtual >= labirintos.length) {
                mostrarTela("telaFinal");
                return;
            }
            posicaoJogador = { linha: 1, coluna: 1 };
        }
        carregarLabirinto();
    }
}

//Movimentação
document.addEventListener("keydown", (e) => {
    const key = e.key.toLowerCase();

    if (["arrowup", "arrowdown", "arrowleft", "arrowright", "w", "a", "s", "d"].includes(key)) {
        e.preventDefault();
        switch (key) {
            case "arrowup":
            case "w":
                moverJogador(-1, 0);
                break;
            case "arrowdown":
            case "s":
                moverJogador(1, 0);
                break;
            case "arrowleft":
            case "a":
                moverJogador(0, -1);
                break;
            case "arrowright":
            case "d":
                moverJogador(0, 1);
                break;
        }
    }
});

// Iniciar o jogo diretamente após o login
document.addEventListener('DOMContentLoaded', () => {
    const user = JSON.parse(localStorage.getItem('loggedInUser'));
    if (!user) {
        window.location.href = 'login.html';
        return;
    }
    nomeJogador = user.nome;
    document.getElementById("exibeNome").innerText = `Olá, ${nomeJogador}!`;
    mostrarTela("telaJogo");
    carregarLabirinto();
});
